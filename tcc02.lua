t=0
s=math.sin
function TIC()
t=t+1
cls(9)
x=120
k=137
w=10
r=1
o=1
rect(x-5,x,10,40,3)
for y=10,x,5 do
tri(x,y-9,x+w,y,x-w,y,6)
w=w+2
if(y>20) then
circ(x+o,y-9,3,r)
r=(r+7)%9+1
o=(o+k)%w-w/2
end
end
for y=1,k do
pix(99*y%240,(t+y)%k,12)
rect(4*y,k-s(y)+s(10*y/3)-t/x,4,999,12)
end
end
